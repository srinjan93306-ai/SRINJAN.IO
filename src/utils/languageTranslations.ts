export const getTranslation = (language: string, key: string): string => {
  const translations = languageTranslations[language as keyof typeof languageTranslations];
  return translations?.[key as keyof typeof translations] || languageTranslations.en[key as keyof typeof languageTranslations.en] || key;
};

export const translateCode = (code: string, fromLang: string, toLang: string): string => {
  if (fromLang === toLang) return code;
  
  let translatedCode = code;
  const fromTranslations = languageTranslations[fromLang as keyof typeof languageTranslations];
  const toTranslations = languageTranslations[toLang as keyof typeof languageTranslations];
  
  if (fromTranslations && toTranslations) {
    Object.keys(fromTranslations).forEach(key => {
      const fromWord = fromTranslations[key as keyof typeof fromTranslations];
      const toWord = toTranslations[key as keyof typeof toTranslations];
      const regex = new RegExp(`\\b${fromWord}\\b`, 'gi');
      translatedCode = translatedCode.replace(regex, toWord);
    });
  }
  
  return translatedCode;
};